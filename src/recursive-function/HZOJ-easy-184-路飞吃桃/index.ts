export const fn = (n: number): number => {
  if (n === 1) {
    return 1
  }

  return 2 * (fn(n - 1) + 1)
};

// 思路：
// 1.定义 —— f(n): 能吃n天的桃子数量

// 2.边界条件 —— f(1) = 1

// 3.本层函数逻辑 —— f(n) = f(n-1) + 第1天吃的桃子数量
//       当前桃子数量     吃的桃子数量        剩余桃子数量
// 第1天  10            (10 / 2) + 1      4
// 第2天  4             (4 / 2) + 1       1
// 第3天  1
// f(n) = f(n - 1) + (f(n) / 2) + 1 即 10 = 4 + (10 / 2) + 1
// f(n) = 2(f(n - 1) + 1)
