import { describe, it, expect } from 'vitest';
import { fn } from '.';

describe('士兵', () => {
  it('[[1, 2], [2, 2], [1, 3], [3, -2], [3, 3]]', () => {

    expect(fn([[1, 2], [2, 2], [1, 3], [3, -2], [3, 3]])).toBe(8)
  })

  it('[[1, 2], [3, 2], [5, 2], [7, 2], [15, 2]]', () => {

    expect(fn([[1, 2], [3, 2], [5, 2], [7, 2], [15, 2]])).toBe(12)
  })
})
